<template>
  <div class="overflow-y-auto bg-white">
    <modal
      :visible="visible"
      style="height: 95%"
      class="w-4/12 flex flex-col overflow-y-auto ml-auto mr-2"
    >
      <div class="flex w-full overflow-y-auto rounded-t-lg p-5">
        <span class="block pr-2 border-r-2">
          <arrow-left-icon
            class="stroke-current text-primary cursor-pointer"
            @click="show = false"
          />
        </span>
          <h2 class="font-bold text-lg text-primary ml-3 -mt-2">Practitioner</h2>
      </div>
      <div class="flex flex-col p-3">
        <p class="text-sm mt-2">
          Select preferred provider
        </p>
         <icon-input autocomplete="off" class="border border-gray-600 rounded-full focus:outline-none"  type="search" placeholder="Search" v-bind="$attrs" v-model="displayVal">
            <template v-slot:prepend>
              <search-icon />
            </template>
        </icon-input>
        <div class="my-2 border-2 w-full flex-col rounded-md flex" v-for="(item,index) in columnsProxy" :key="index">
            <span
              class="items-center hover:bg-gray-100 w-full flex justify-between"
             >
              <label class="flex items-center justify-between py-3 px-3">
                <input
                  v-model="columnsProxy[index].show"
                  type="checkbox"
                  @input="changed"
                  class="bg-danger focus-within:bg-danger px-6 shadow"
                />
                <span>
                <span class="text-xs font-bold float-left pl-3">{{ item.firstName }} {{ item.lastName }}</span>
                <span class="text-xs text-gray-300 font-bold float-left pl-3">{{ item.jobDesignation }},{{ item.department }}</span>
                </span>
              </label>
              <div class="flex  mr-4">
                <p class="cursor-pointer mr-2 hover:shadow-lg text-xs text-danger">View Availability</p>
                <p class="cursor-pointer mr-2 hover:shadow-lg text-xs text-danger">View Profile</p>
              </div>
            </span>
        </div>
        <div class="flex justify-end w-full mt-auto">
          <button
            class="
              rounded-full
              mt-5
              py-2
              px-3
              border border-primary
              focus:outline-none
              hover:opacity-90
              w-1/3
              mr-2
              text-primary
              font-semibold
            "
            @click="show = false"
          >
            Cancel
          </button>
          <button
            @click="apply"
            class="
              bg-danger
              rounded-full
              text-white
              mt-5
              py-2
              px-3
              focus:outline-none
              hover:opacity-90
              w-1/3
            "
          >
            Apply
          </button>
        </div>
      </div>
    </modal>
  </div>
</template>
<script>
import Modal from "@/components/practitionermodal.vue";
import ArrowLeftIcon from "@/components/icons/arrowleft.vue";
import DragIcon from "@/components/icons/draggable.vue";
import Draggable from "vuedraggable";
import IconInput from "@/components/IconInput.vue";
import SearchIcon from "@/components/icons/search.vue";

const copy = (original) => JSON.parse(JSON.stringify(original));

export default {
  name: "ParticipantFilter",
  components: {
    Modal,
    DragIcon,
    ArrowLeftIcon,
    Draggable,
    IconInput,
    SearchIcon
  },
  props: {
    visible: {
      type: Boolean,
      required: true,
      default: false,
    },
    columns: {
      type: Array,
      required: true,
      default: () => [],
    },
    preferred: {
      type: Array,
      required: true,
      default: () => [],
    },
  },
  data() {
    return {
      columnsProxy: [],
      practitioners: [],
    };
  },
  watch: {
    columns(val) {
      this.columnsProxy = copy(val);
    },
    visible() {
      const active = this.preferred.length > 0 ? this.preferred : this.columns;
      this.columnsProxy = copy([...active]);
    },
  },
  computed: {
    show: {
      get() {
        return this.visible;
      },
      set(val) {
        this.$emit("update:visible", val);
      },
    },
  },
  methods: {
    apply() {
      this.$emit("update:preferred", copy([...this.columnsProxy]));
      this.show = false;
    },
    reset() {
      this.$emit("update:preferred", copy([...this.columns]));
      this.show = false;
    },
  },
  mounted() {
    this.columnsProxy = copy([...this.columns]);
  },
};
</script>
