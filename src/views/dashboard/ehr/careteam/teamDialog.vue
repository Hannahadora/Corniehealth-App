const careteam = namespace("careteam");
const dropdown = namespace("dropdown");
const patients = namespace("patients");
const group = namespace("group");

@Options({
  name: "careTeamModal",
  components: {
    ...CornieCard,
    AccordionComponent,
    CornieIconBtn,
    ArrowLeftIcon,
    InfoBlueBg,
    DRangePicker,
    PhoneInput,
    FhirInput,
    CancelIcon,
    AutoComplete,
    CornieDialog,
    CornieRadio,
    AddActor,
    Avatar,
    SelectOption,
    SearchIcon,
    TextArea,
    DeleteIcon,
    DatePicker,
