    DeleteorangeIcon,
    ReferenceModal,
    plusIcon
  },
})
export default class careTeamModal extends Vue {
  @PropSync("modelValue", { type: Boolean, default: false })
  show!: boolean;

  @Prop({ type: String, default: "" })
  id!: string;

  @careteam.Action
  getCareteamById!: (id: string) => ICareteam;

  @patients.State
  patients!: IPatient[];

  @patients.Action
  fetchPatients!: () => Promise<void>;

  @group.State
  groups!: IGroup[];

  @group.Action
  fetchGroups!: () => Promise<void>;

  requiredRule = string().required();

  loading = false;
  showRefModal = false;
  DialCode = "+234";
  PhoneNumber = "";

  subjectpatient = "";
  subjectgroup = "";

  identifier = "";
  basedOn = "Patient";
  replaces = "";
  startDateTime = "";
  endDateTime = "";
  startDateTimePeriod = "";
  endDateTimePeriod = "";
  startDateTimeCom = "";
  endDateTimeCom = "";
  startDateCom = "";
  endDateCom = "";
  value = "";
  reasonCode = "";
  reasonReference = "";
  localSrc = require("../../../../assets/img/placeholder.png");
  use = "Home";
  system = "Phone";
  subject = "Patient";
  search = "";
  onBehalfOf = "";
  role = "";
  member = "";
  notes = "";
  data: any = {
    days: [],
  };

  communication = [] as IIndexableObject[];

  dropdowns = {} as IIndexableObject;

  @dropdown.Action
  getDropdowns!: (a: string) => Promise<IIndexableObject>;

  status = "active";
  category = "";
  name = "";
  participants: Participants[] = [];
  period = { start: "", end: "" };
  emailRule = string().email().required();
  required = string().required();
  fax = "";
  Actors = [] as any;

  phone = {
    number: "",
    dialCode: "+234",
  } as any;
  note = "";
  email = "";
  Identifier = "";

  async handleSystemChange(val: string) {
    this.system = val;
    console.log(val);
  }

  async addCommunication() {
    if (
      this.system === "" ||
      this.value === "" ||
      this.use === "" ||
      this.startDateTimeCom === "" ||
      this.endDateTimeCom === ""
    )
      return;

    let info = {
      system: this.system,
      value: this.value,
      use: this.use,
      startDate: new Date(this.startDateTimeCom).toLocaleString(),
      endDate: new Date(this.endDateTimeCom).toLocaleString(),
    } as IIndexableObject;

    this.communication = [info, ...this.communication];
    this.system = "Phone";
    this.use = "Home";

    this.value = this.startDateTimeCom = this.endDateTimeCom = "";
  }

  get activePatientId() {
    const id = this.$route?.params?.id as string;
    return id;
  }

  refvalue(value:any, type:string){
    //this.references.push(value);
    this.reasonReference = type;
  }

  actorslist(value: any) {
    this.Actors = value;
  }
  removeActor(index: number) {
    this.Actors.splice(index, 1);
  }
  @Watch("id")
  idChanged() {
    this.setCareteam();
  }

  async setCareteam() {
    const careteam = await this.getCareteamById(this.id);
    if (!careteam) return;
    this.identifier = careteam.identifier;
    this.status = careteam.status;
    this.category = careteam.category;
    this.name = careteam.name;
    this.subject = careteam.subject;
    this.period = careteam.period;
    this.Actors = careteam.participants;
    this.reasonCode = careteam.reasonCode;
    this.reasonReference = careteam.reasonReference;
    this.note = careteam.note;
    this.phone = careteam.phone;
    this.email = careteam.email;
  }

  get payload() {
    return {
      identifier: this.identifier,
      status: this.status,
      category: this.category,
      name: this.name,
      subject: this.subject,
      period: this.period,
      participants: this.Actors,
      reasonCode: this.reasonCode,
      reasonReference: this.reasonReference,
      note: this.note,
      phone: this.phone,
      email: this.email,
      patientId: this.activePatientId,
    };
  }

  get newaction() {
    return this.id ? "Update" : "Create";
  }

  async submit() {
    this.loading = true;
    if (this.id) await this.updateCareteam();
    else await this.createCareteam();
    this.loading = false;
  }

  async createCareteam() {
    this.payload.period.start = new Date(
      this.payload.period.start
    ).toISOString();
    this.payload.period.end = new Date(this.payload.period.end).toISOString();
   
    try {
      const response = await cornieClient().post(
        "/api/v1/care-teams",
        this.payload
      );
      if (response.success) {
        window.notify({ msg: "Care team added", status: "success" });
        this.done();
      }
    } catch (error: any) {
      window.notify({
        msg: error.response.data.message,
        status: "error",
      });
    }
  }

  async updateCareteam() {
    const url = `/api/v1/care-teams/${this.id}`;
    const payload = { ...this.payload };
    try {
      const response = await cornieClient().put(url, payload);
      if (response.success) {
        window.notify({ msg: "Care team updated", status: "success" });
        this.done();
      }
    } catch (error: any) {
      window.notify({
        msg: error.response.data.message,
        status: "error",
      });
    }
  }

  get allPatients() {
    if (!this.patients || this.patients.length === 0) return [];
    return this.patients.map((i: any) => {
      return {
        code: i.id,
        display: `${i.firstname} ${i.lastname}`,
      };
    });
  }

  get allGroups() {
    if (!this.groups || this.groups.length === 0) return [];
    return this.groups.map((i: any) => {
      return {
        code: i.id,
        display: `${i.name}`,
      };
    });
  }

  async fetchOrgInfo() {
    try {
      const response = await cornieClient().get(
        "/api/v1/organization/myOrg/get"
      );
      this.identifier = response.data.identifier;
    } catch (error) {
      alert("Could not fetch organization");
    }
  }
  done() {
    this.$emit("careteam-added");
    this.show = false;
  }

  async created() {
    await this.setCareteam();
    await this.fetchPatients();
    await this.fetchGroups();
    this.fetchOrgInfo();
    const data = await this.getDropdowns("careTeam");
    this.dropdowns = data;
  }
}
</script>
