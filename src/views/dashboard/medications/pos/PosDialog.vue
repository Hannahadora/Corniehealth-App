        <cornie-card-text class="flex-grow scrollable">
          <v-form class="flex-grow flex flex-col" @submit="submit">
            <div class="grid grid-cols-2 gap-6">
              <cornie-input
                class="w-full"
                label="Reference"
                placeholder="Autogenerated"
                v-model="refernce"
                :disabled="true"
              />
              <date-picker
                class="w-full"
                label="Sales Date"
                v-model="salesDate"
              />

              <div class="relative">
                <span
                  class="text-red-500 float-right text-sm absolute right-0 cursor-pointer"
                  >Add</span
                >
                <auto-complete
                  v-bind="$attrs"
                  label="Customer"
                  placeholder="Select one"
                  v-model="customer.name"
                  :items="customers"
                  @query="fetchCustomers"
                >
                  <template #item="{ item }">
                    <div class="w-full flex items-center my-1 justify-between">
                      <div class="flex items-center">
                        <!-- <avatar :src="item.image" /> -->
                        <div class="flex ml-1 flex-col">
                          <span class="text-xs">{{ item.name }}</span>
                        </div>
                      </div>
                      <span class="text-xs font-semibold text-gray-500">
                        {{ item.mrn }}
                      </span>
                    </div>
                  </template>
                </auto-complete>
              </div>

              <cornie-select
                class="w-full"
                label="Type"
                placeholder="--Select--"
                v-model="type"
                :items="['Store Pick-Up', 'Delivery']"
              />
            </div>

            <div class="flex items-center justify-end mt-12">
              <cornie-btn
                type="submit"
                class="text-white bg-danger px-12 rounded-lg"
                @click.prevent="addMedication"
              >
                Add
              </cornie-btn>
            </div>
          </v-form>

          <cornie-table :columns="rawHeaders" v-model="items" class="mt-7">
            <template #actions="{ item }">
              <div
                class="flex items-center hover:bg-gray-100 p-3 cursor-pointer"
                @click="showItem(item.id)"
              >
                <edit-icon class="text-danger fill-current" />
                <span class="ml-3 text-xs">Update</span>
              </div>
              <div
                class="flex items-center hover:bg-gray-100 p-3 cursor-pointer"
                @click="deleteItem(item.id)"
              >
                <delete-icon class="text-danger fill-current" />
