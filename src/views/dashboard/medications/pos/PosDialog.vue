import { debounce } from "lodash";

import search from "@/plugins/search";

const orgFunctions = namespace("OrgFunctions");
const user = namespace("user");

@Options({
  name: "POSDialog",
  components: {
    CornieDialog,
    ...CornieCard,
    ArrowLeft,
    IconBtn,
    CornieInput,
    CornieSelect,
    CustomCheckbox,
    CornieBtn,
    AutoComplete,
    CornieRadio,
    DatePicker,
    SearchInput,
    CornieTable,
    TableOptions,
    FullPayment,
    SplitPayment,
    CancelRedBg,
    CornieSearch,
    PlusIconWhite,
    AddCustomer,
    AddMedications,
    DeleteIcon,
    EditIcon,
    QuantityInput,
    corninuminput,
  },
})
export default class PosDialog extends Vue {
  @PropSync("modelValue", { type: Boolean, default: false })
  show!: boolean;

  @Prop({ type: String, default: "" })
  id!: string;

  @Prop({ type: Object, default: "" })
  sales!: IPossale;

  @Prop({ type: Boolean, default: false })
  salesData!: boolean;

  required = string().required();

  showDatalist = false;
  searchMedication = false;
  discount = 0;

  query = "";
  addCustomerModal = false;

  loading = false;
  activeTab = "Full Payment";
  customerDetails = <any>[];

  status = "";
  type = "";
  coupon = undefined;
  customerId = "";
  customer = {
    name: "",
  };
  reference = "";
  salesDate = "";
  medications = <any>[];

  @user.Getter
  authCurrentLocation!: any;

  getKeyValue = getTableKeyValue;
  preferredHeaders = [];
  rawHeaders = [
    {
      title: "ITEM NAME",
      key: "itemName",
      show: true,
    },
    {
      title: "UNIT PRICE",
      key: "unitPrice",
      show: true,
    },
    {
      title: "QUANTITY",
      key: "quantity",
      show: true,
    },
    {
      title: "LINE TOTAL",
      key: "lineTotal",
      show: true,
    },
  ];

  get checkSales() {
    if (Object.entries(this.sales).length > 0) {
      return true;
    }
    return false;
  }

  // get locationId() {
  //   return this.authCurrentLocation;
  // }

  get locationId() {
    return "21b84341-2051-4cad-b6b6-feae04f81215";
  }

  get items() {
    const dMed = this.medications?.map((medication: any) => {
      return {
        ...medication,
        action: medication.id,
        itemName: medication.name,
        unitPrice: medication.unitPrice,
        quantity: medication.quantity,
        // lineTotal: medication.lineTotal,
        // lineTotal: Number(medication.unitPrice * medication.quantity),
      };
    });
    if (!this.query) return dMed;
    return search.searchObjectArray(dMed, this.query);
  }

  @Watch("items", { immediate: true, deep: true })
  getLineTotal() {
    this.items.forEach((item: any) => {
      item.lineTotal = item.quantity * item.unitPrice;
    });
  }

  get totalDiscount() {
    // if (this.discount) {
    //   const dP = this.items?.map(
    //     (item: any) => item.lineTotal * (this.discount / 100)
    //   ); 
    //   return dP.reduce((a: any, b: any) => a + b, 0).toFixed(2);
    // } else return 0;
    return 0;
  }

  get shippingCost() {
    return 0;
  }

  get subTotal() {
    const lT = this.items?.map((item: any) => item.lineTotal);
    const sT = lT.reduce((a: any, b: any) => a + b, 0);
    return Number(sT - this.totalDiscount).toFixed(2);
  }

  get grandTotal() {
    return Number(this.subTotal + this.shippingCost).toFixed(2);
  }
  
  get payments() {
    return [
      {
        amount: this.grandTotal,
        paymentType: "",
        total: this.grandTotal,
      },
    ];
  }

  fetchCustomers(query: string) {
    debounce(async () => {
      try {
        const { data } = await cornieClient().get(
          `/api/v1/pharmacy/find-customer/?query=${query}`
        );
        this.customerDetails = data || [];
      } catch (error) {
        window.notify({
          msg: "There was an error fetching customers details",
          status: "error",
        });
      }
    }, 1000)();
  }

  get customers() {
    const xCustomers = this.customerDetails?.map((customer: any) => {
      return {
        ...customer,
        code: customer.id,
        display: customer.name,
        name: customer.name,
        mrn: customer.mrn,
        id: customer.id,
      };
    });
    if (!this.query) return xCustomers;
    return search.searchObjectArray(xCustomers, this.query);
  }

  get patientName() {
    const pId = this.customerDetails.find((el: any) => {
      el.id === this.customerId;
    });
    return pId.name;
  }

  addMedication(chosenMedication: any) {
    this.medications.push({ ...chosenMedication, quantity: 1 });
  }

  async addSales(type: any) {
    if (type === 0) {
      this.status = "on-hold";
    } else if (type === 1) {
      this.status = "completed";
    }
    const newSales = {
      patientId: this.customerId,
      type: this.type,
      status: this.status,
      coupon: this.coupon,
      medications: this.medications.map((el: any) => {
        return {
          ...el,
          dispensedProductId: el.id,
          reasonForSubstitution: el.reasonForSubstitution,
          quantity: el.quantity,
        };
      }),
      payments: this.payments,
      customer: {
        name: this.customerId,
      },
    };
    try {
      const { data } = await cornieClient().post(
        `/api/v1/pharmacy/pos-dispense/${this.locationId}`,
        { ...newSales }
      );
      this.$emit("salesAdded");
    } catch (error) {
      window.notify({
        msg: "An error occured",
        status: "error",
      });
    }
  }

  setData() {
    if (this.sales) {
      (this.customerId as any) = this.sales?.customer;
      ((this.customer.name as any) = this.sales?.customer),
        (this.reference = this.sales?.identifier);
      this.salesDate = new Date(this.sales?.createdAt).toLocaleDateString(
        "en-GB"
      );
      this.medications = this.sales?.medications;
    }
  }

  deleteItem(index: any) {
    // this.medications.find((el: any) => {
    //   el.id === itemId;
    //   this.medications.splice(el);
    // });
    // this.medications.filter((el: any) => el.id !== itemId)
      this.medications.splice(index, 1);
  }

  showItem(id: any) {}

  created() {
    if (this.id) {
      this.setData();
    }
  }
}
</script>

