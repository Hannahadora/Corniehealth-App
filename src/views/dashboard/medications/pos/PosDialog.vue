<script lang="ts">
import { Options, Vue } from "vue-class-component";
import CornieDialog from "@/components/CornieDialog.vue";
import CornieCard from "@/components/cornie-card";
import ArrowLeft from "@/components/icons/arrowleft.vue";
import CancelIcon from "@/components/icons/cancel.vue";
import IconBtn from "@/components/CornieIconBtn.vue";
import CornieInput from "@/components/cornieinput.vue";
import CornieSelect from "@/components/cornieselect.vue";
import CustomCheckbox from "@/components/custom-checkbox.vue";
import { Prop, PropSync, Watch } from "vue-property-decorator";
import CornieBtn from "@/components/CornieBtn.vue";
import { namespace } from "vuex-class";
import { CornieUser } from "@/types/user";
import { string } from "yup";
import AutoComplete from "@/components/autocomplete.vue";
import { cornieClient2 } from "@/plugins/http";
import CornieRadio from "@/components/cornieradio.vue";
import IAppointmentRoom from "@/types/IAppointmentRoom";

import DatePicker from "@/components/datepicker.vue";
import SearchInput from "@/components/search-input.vue";
import { first, getTableKeyValue } from "@/plugins/utils";

import TableOptions from "@/components/table-options.vue";
import CornieTable from "@/components/cornie-table/CornieTable.vue";
import FullPayment from "./components/FullPayment.vue";
import SplitPayment from "./components/SplitPayment.vue";

import search from "@/plugins/search";

const hierarchy = namespace("hierarchy");
const orgFunctions = namespace("OrgFunctions");
const user = namespace("user");
const appointmentRoom = namespace("appointmentRoom");

@Options({
  name: "POSDialog",
  components: {
    CornieDialog,
    ...CornieCard,
    ArrowLeft,
    IconBtn,
    CornieInput,
    CornieSelect,
    CustomCheckbox,
    CornieBtn,
    AutoComplete,
    CornieRadio,
    DatePicker,
    SearchInput,
    CornieTable,
    TableOptions,
    FullPayment,
    SplitPayment,
    CancelIcon,
  },
})
export default class AppointmentRoomDialog extends Vue {
  @PropSync("modelValue", { type: Boolean, default: false })
  show!: boolean;

  @Prop({ type: String, default: "" })
  id!: string;

  required = string().required();

  query = "";

  loading = false;
  activeTab = "Full Payment";
  customers = <any>[];

  status = "";
  type = "";
  coupon = "";
  customer = {
    name: "",
  };
  reference = "";
  salesDate = "";
  medications = <any>[];
  payments = [
    {
      amount: "",
      paymentType: "",
    },
  ];

  @user.Getter
  authCurrentLocation!: any;

  getKeyValue = getTableKeyValue;
  preferredHeaders = [];
  rawHeaders = [
    {
      title: "ITEM NAME",
      key: "itemName",
      show: true,
    },
    {
      title: "UNIT PRICE",
      key: "unitPrice",
      show: true,
    },
    {
      title: "QUANTITY",
      key: "quantity",
      show: true,
    },
    {
      title: "LINE TOTAL",
      key: "lineTotal",
      show: true,
    },
  ];

   get patientId() {
    return (this.$route.params.id || this.$route.params.patientId) as string;
  }

  get locationId() {
    return this.authCurrentLocation;
  }

  get items() {
    const dMed = this.medications?.map((medication: any) => {
      return {
        ...medication,
        action: medication.id,
        itemName: medication.name,
        unitPrice: medication.unitPrice,
        quantity: medication.quantity,
        lineTotal: medication.total,
      };
    });
    if (!this.query) return dMed;
    return search.searchObjectArray(dMed, this.query);
  }

  async fetchCustomers(query: string) {
    try {
      const { data } = await cornieClient2().get(
        `/api/v1/pharmacy/find-customer/?query=${query}`
      );
      this.customers =
        data[0].map((customer: any) => ({
          ...customer,
          code: customer.id,
          name: customer.name,
          mrn: customer.mrn,
          // display: printCustomer(customer),
        })) || [];
    } catch (error) {
      window.notify({
        msg: "There was an error fetching customers details",
        status: "error",
      });
    }
  }

  addMedication() {
    const newM = {
      reference: this.reference,
      salesDate: this.salesDate,
      customer: this.customer,
      type: this.type,
    };
    this.medications.push({ ...newM });
    this.reference = "";
    this.salesDate = "";
    this.customer.name = "";
    this.type = "";
  }

  async addSales(type: any) {
    if(type === 0) {
      this.status = 'on-hold'
    }else if (type === 1){
      this.status = 'completed'
    }
    const salesData = {
      patientId: this.patientId,
      type: this.type,
      status: this.status,
      coupon: this.coupon,
      medications: this.medications.map((el: any) => {
        return {
          ...el,
          dispensedProductId: el.dispensedProductId,
          reasonForSubstitution: el.reasonForSubstitution,
          quantity: el.quantity,
        };
      }),
      payments: this.payments.map((el: any) => {
        return {
          ...el,
          amount: el.amount,
          paymentType: el.paymentType,
        };
      }),
      customer: this.customer,
    };
    try {
      const { data } = await cornieClient2().post(
        `/api/v1/pharmacy/pos-dispense/${this.locationId}`,
        { ...salesData }
      );
    } catch (error) {
      window.notify({
        msg: "An error occured",
        status: "error",
      });
    }
  }

  async created() {}
}
</script>

