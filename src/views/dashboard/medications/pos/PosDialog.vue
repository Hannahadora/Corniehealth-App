      class="flex flex-col h-full bg-white px-6 overflow-y-scroll"
    >
      <cornie-card-title class="">
        <icon-btn @click="show = false">
          <arrow-left stroke="#ffffff" />
        </icon-btn>
        <div class="w-full">
          <h2 class="font-bold float-left text-lg text-primary ml-3 -mt-1">
            New Sales
          </h2>
          <cancel-icon
            class="float-right cursor-pointer"
            @click="show = false"
          />
        </div>
      </cornie-card-title>
      <div class="border px-4 py-6">
        <cornie-card-text class="flex-grow scrollable">
          <v-form class="flex-grow flex flex-col" @submit="submit">
            <div class="grid grid-cols-2 gap-6">
              <cornie-input
                class="w-full"
                label="Reference"
                placeholder="Autogenerated"
                v-model="refernce"
                :disabled="true"
              />
              <date-picker
                class="w-full"
                label="Sales Date"
                v-model="salesDate"
                :disabled="salesData"
              />

              <div class="relative">
                <span
                  class="z-20 text-red-500 float-right font-bold text-sm absolute right-0 cursor-pointer"
                  @click.stop="addCustomerModal = true"
                  >Add</span
                >
                <auto-complete
                  v-bind="$attrs"
                  label="Customer"
                  placeholder="Select one"
                  v-model="customerId"
                  :rules="required"
                  :items="customers"
                  @query="fetchCustomers"
                  :disabled="salesData"
                >
                  <template #item="{ item }">
                    <div class="w-full flex items-center my-1">
                      <!-- <avatar :src="item.image" /> -->
                      <div class="ml-4 flex flex-col">
                        <span class="text-xs">{{ item.name }}</span>
                        <span class="text-xs font-semibold text-gray-500">
                          {{ item.mrn }}
                        </span>
                      </div>
                    </div>
                  </template>
                </auto-complete>
              </div>

              <cornie-select
                class="w-full"
                label="Type"
                placeholder="--Select--"
                v-model="type"
                :items="['pickup', 'delivery']"
                :disabled="salesData"
              />
            </div>

            <div
              v-if="!salesData || !checkSales"
              class="flex items-center justify-between mt-12"
            >
              <div class="">
                <cornie-search
                  class="w-full rounded-full mb-3"
                  placeholder="Search Item by name or code"
                  v-model="medQuery"
                  :results="medicationData"
                >
                </cornie-search>
              </div>
              <cornie-btn
                type="submit"
                class="text-white bg-danger px-12 rounded-lg"
                @click.prevent="addMedication"
              >
                <plus-icon-white class="mr-3" />
                Add
              </cornie-btn>
            </div>
          </v-form>

          <cornie-table
            :columns="rawHeaders"
            v-model="items"
            :listmenu="true"
            :check="false"
            class="mt-7"
          >
            <template #actions="{ item }">
              <div
                class="flex items-center hover:bg-gray-100 p-3 cursor-pointer"
                @click="showItem(item.id)"
              >
                <edit-icon class="text-danger fill-current" />
                <span class="ml-3 text-xs">Update</span>
              </div>
              <div
                class="flex items-center hover:bg-gray-100 p-3 cursor-pointer"
                @click="deleteItem(item.id)"
              >
                <delete-icon class="text-danger fill-current" />
                <span class="ml-3 text-xs">Delete</span>
              </div>
            </template>
          </cornie-table>

          <div class="mt-8 flex items-start justify-between">
            <div class="w-1/3">
              <div
                class="px-4 py-3 text-sm border rounded-md flex items-center justify-between w-full"
              >
                <span class="w-3/5 text-xs">Coupon | Promo Code</span>
                <input
                  class="w-2/5 self-end"
                  type="text"
                  v-model="coupon"
                  placeholder="----"
                  :disabled="salesData"
                />
              </div>
              <span class="text-xs text-red-500 mt-2 text-right"
                >A Coupon/Promo discount of 20% applies.</span
              >
            </div>

            <div class="w-1/3">
              <table class="w-full">
                <tbody>
                  <tr>
                    <td>Total Discount</td>
                    <td>----</td>
                  </tr>
                  <tr>
                    <td>Sub Total</td>
                    <td>----</td>
                  </tr>
                  <tr>
                    <td class="font-bold">Shipping Cost</td>
                    <td>----</td>
                  </tr>
                  <tr class="">
                    <td class="font-bold">Grand Total</td>
                    <td>----</td>
                  </tr>
                </tbody>
              </table>
            </div>
          </div>

          <div class="mt-7">
            <div class="w-full border-gray-300 border-b flex items-center mb-6">
              <span
                class="cursor-pointer px-4 py-1 text-gray-400"
                :class="{
                  'border-red-500 border-b-4 text-black':
                    activeTab === 'Full Payment',
                }"
                @click="activeTab = 'Full Payment'"
              >
                Full Payment
              </span>
              <span
                class="cursor-pointer px-4 py-1 text-gray-400"
                :class="{
                  'border-red-500 border-b-4 text-black':
                    activeTab === 'Split Payment',
                }"
                @click="activeTab = 'Split Payment'"
              >
                Split Payment
              </span>
            </div>

            <div>
              <full-payment
                v-if="activeTab === 'Full Payment'"
                :payments="payments"
                :salesData="salesData"
              />
              <split-payment
                v-if="activeTab === 'Split Payment'"
                :payments="payments"
                :salesData="salesData"
              />
            </div>
          </div>
        </cornie-card-text>
      </div>

      <div
        class="flex items-center justify-between mt-14"
        v-if="checkSales && sales.status !== 'completed'"
      >
        <div>
          <span
            class="text-red-500 font-bold text-base cursor-pointer"
            @click="addSales(0)"
            >Keep On Hold</span
          >
        </div>
        <div class="flex items-center mb-6">
          <cornie-btn
            @click="show = false"
            class="border-primary border-2 px-6 py-1 mr-3 rounded-lg text-primary"
          >
            Cancel
          </cornie-btn>
          <cornie-btn
            :loading="loading"
            type="submit"
            class="text-white bg-danger px-3 py-1 rounded-lg"
            @click="addSales(1)"
          >
            Completed Sales
          </cornie-btn>
        </div>
      </div>
    </cornie-card>
  </cornie-dialog>

  <add-customer v-model="addCustomerModal" />
