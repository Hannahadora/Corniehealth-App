                  <tr class="">
                    <td class="font-bold">Grand Total</td>
                    <td>----</td>
                  </tr>
                </tbody>
              </table>
            </div>
          </div>

          <div class="mt-7">
            <div class="w-full border-gray-300 border-b flex items-center mb-6">
              <span
                class="cursor-pointer px-4 py-1 text-gray-400"
                :class="{
                  'border-red-500 border-b-4 text-black':
                    activeTab === 'Full Payment',
                }"
                @click="activeTab = 'Full Payment'"
              >
                Full Payment
              </span>
              <span
                class="cursor-pointer px-4 py-1 text-gray-400"
                :class="{
                  'border-red-500 border-b-4 text-black':
                    activeTab === 'Split Payment',
                }"
                @click="activeTab = 'Split Payment'"
              >
                Split Payment
              </span>
            </div>

            <div>
              <full-payment
