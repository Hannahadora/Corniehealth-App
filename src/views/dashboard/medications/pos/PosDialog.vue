    CornieSearch,
    PlusIconWhite,
    AddCustomer,
    IconInput,
    SearchIcon,
  },
})
export default class PosDialog extends Vue {
  @PropSync("modelValue", { type: Boolean, default: false })
  show!: boolean;

  @Prop({ type: String, default: "" })
  id!: string;

  @Prop({ type: Object, default: <any>{} })
  sales!: any;

  @Prop({ type: Boolean, default: false })
  salesData!: boolean;

  required = string().required();

  activeMedIndex = 0;

  showDatalist = false;
  quantity = 1;

  query = "";
  medQuery = "";
  addCustomerModal = false;
  selectedMed = {};

  loading = false;
  activeTab = "Full Payment";
  customerDetails = <any>[];
  medicationDetails = <any>[];

  status = this.sales?.status || "";
  type = this.sales?.type || "";
  coupon = this.sales?.coupon || "";
  customerId = "";
  customer = {
    name: this.sales?.customer?.name || "",
  };
  reference = this.sales?.reference || "";
  salesDate = this.sales?.createdAt || "";
  medications = this.sales?.medications || <any>[];
  medication = "";
  payments = this.sales?.payments || [
    {
      amount: "",
      paymentType: "",
      total: 0,
    },
  ];

  @user.Getter
  authCurrentLocation!: any;

  getKeyValue = getTableKeyValue;
  preferredHeaders = [];
  rawHeaders = [
    {
      title: "ITEM NAME",
      key: "itemName",
      show: true,
    },
    {
      title: "UNIT PRICE",
      key: "unitPrice",
      show: true,
    },
    {
      title: "QUANTITY",
      key: "quantity",
      show: true,
    },
    {
      title: "LINE TOTAL",
      key: "lineTotal",
      show: true,
    },
  ];

  get checkSales() {
    if (Object.entries(this.sales).length === 0) {
      return true;
    }
    return false;
  }

  // get locationId() {
  //   return this.authCurrentLocation;
  // }

  get locationId() {
    return "21b84341-2051-4cad-b6b6-feae04f81215";
  }

  @Watch("quantity")
  updateLineTotal() {
    this.medications?.map((medication: any, i: number) => {
      return {
        ...medication,
        lineTotal: ((medication.unitPrice as number) *
          medication.quantity) as number,
      };
    });
  }

  get items() {
    const dMed = this.medications?.map((medication: any, i: number) => {
      return {
        ...medication,
        action: medication.id,
        itemName: medication.name,
        unitPrice: medication.unitPrice,
        quantity: this.quantity,
        lineTotal: ((medication.unitPrice as number) *
          medication.quantity) as number,
      };
    });
    if (!this.query) return dMed;
    return search.searchObjectArray(dMed, this.query);
  }

  selectMed(item: any) {
    this.$emit("selected", item);
    console.log("item", item);
    this.selectedMed = item;
    this.showDatalist = false;
  }

  fetchCustomers(query: string) {
    debounce(async () => {
      try {
        const { data } = await cornieClient().get(
          `/api/v1/pharmacy/find-customer/?query=${query}`
        );
        this.customerDetails = data || [];
      } catch (error) {
        window.notify({
          msg: "There was an error fetching customers details",
          status: "error",
        });
      }
    }, 1000)();
  }

  get customers() {
    const xCustomers = this.customerDetails?.map((customer: any) => {
      return {
        ...customer,
        code: customer.id,
        display: customer.name,
        name: customer.name,
        mrn: customer.mrn,
        id: customer.id,
      };
    });
    if (!this.query) return xCustomers;
    return search.searchObjectArray(xCustomers, this.query);
  }

  async fetchMedications(query: string) {
    try {
      const { data } = await cornieClient().get(
        `/api/v1/pharmacy/find-medication/${this.locationId}/?query=${query}`
      );
      this.medicationDetails = data || [];
    } catch (error) {
      window.notify({
        msg: "There was an error fetching medications",
        status: "error",
      });
    }
  }

  get medicationData() {
    const xMedications = this.medicationDetails?.map((medication: any) => {
      return {
        ...medication,
        code: medication.id,
        name: medication.name,
        display: medication.brandCode,
        brandCode: medication.brandCode,
        unitPrice: medication.unitPrice,
        lineTotal: ((medication.unitPrice as number) *
          medication.quantity) as number,
        quantity: 1,
        available: medication.available,
        form: medication.form,
        id: medication.id,
        totalDiscount: medication,
      };
    });
    if (!this.query) return xMedications;
    return search.searchObjectArray(xMedications, this.query);
  }

  get patientName() {
    const pId = this.customerDetails.find((el: any) => {
      el.id === this.customerId;
    });
    return pId.name;
  }

  addMedication() {
    this.medications.push({ ...this.selectedMed });
    this.activeMedIndex = this.medications.length - 1;
  }

  async addSales(type: any) {
    if (type === 0) {
      this.status = "on-hold";
    } else if (type === 1) {
      this.status = "completed";
    }
    const newSales = {
      patientId: this.customerId,
      type: this.type,
      status: this.status,
      coupon: this.coupon,
      medications: this.medications.map((el: any) => {
        return {
          ...el,
          dispensedProductId: el.id,
          reasonForSubstitution: el.reasonForSubstitution,
          quantity: el.quantity,
        };
      }),
      payments: this.payments.map((el: any) => {
        return {
          ...el,
          amount: el.amount,
          paymentType: el.paymentType,
        };
      }),
      customer: {
        name: this.customerId,
      },
    };
    try {
      const { data } = await cornieClient().post(
        `/api/v1/pharmacy/pos-dispense/${this.locationId}`,
        { ...newSales }
      );
    } catch (error) {
      window.notify({
        msg: "An error occured",
        status: "error",
      });
    }
  }

  @Watch("medQuery")
  typed(medQuery: string) {
    this.fetchMedications(medQuery);
  }

  deleteItem(itemId: string) {
    this.medications.filter((el: any) => {
      el.id = itemId;
      this.medications = this.medications.splice(el, 1);
    });
  }

  async created() {}
}
</script>

